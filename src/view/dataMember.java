    public void setColomnWidth(int kolom) { 
        DefaultTableColumnModel dtcm = (DefaultTableColumnModel) tblmodel. getColumnModel();
        TableColumn kolomtabel = dtcm.getColumn(kolom); 
        int lebar = 0; 
        int margin = 45; 
        int a; 
        TableCellRenderer renderer = kolomtabel.getHeaderRenderer(); 
        if (renderer ==null){
            renderer = tblmodel.getTableHeader().getDefaultRenderer();
        } 
        Component komponen = renderer.getTableCellRendererComponent(tblmodel, kolomtabel.getHeaderValue(), false, false, 0, 0); 
        lebar = komponen.getPreferredSize().width; 
        for (a = 0; a< tblmodel.getRowCount(); 
        a++){
            renderer = tblmodel.getCellRenderer(a, kolom); 
            komponen = renderer.getTableCellRendererComponent(tblmodel, tblmodel.getValueAt(a, kolom), false, false, a, kolom); 
            int lebarKolom = komponen.getPreferredSize().width; 
            lebar = Math.max(lebar, lebarKolom);
        } 
        lebar = lebar + margin; 
        kolomtabel.setPreferredWidth(lebar);
    } 
    public void setLebarKolom(){ 
        int a; 
        for(a =0; 
        a< tblmodel.getColumnCount(); 
        a++){ setColomnWidth(a);
        }
    }
